import { mkdir, stat, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { pathToFileURL } from "node:url";
import type { WorkflowConfig } from "@darrylondil/lorien-runtime";

export interface GenerateResult {
  /** Where the .d.ts was written, or null if no lorien.config.ts was found. */
  path: string | null;
  /** Service names that were typed. */
  serviceNames: string[];
}

/**
 * Generates .lorien/types/services.d.ts from <root>/lorien.config.ts.
 * Idempotent: re-running produces the same output for the same config.
 *
 * v1.0 limitation: all services typed as `unknown`. Users can refine by hand
 * or wait for v1.1 which will infer from the value's TS type.
 */
export async function generateServicesTypes(
  root: string,
): Promise<GenerateResult> {
  const configPath = join(root, "lorien.config.ts");
  if (!(await fileExists(configPath))) {
    return { path: null, serviceNames: [] };
  }

  const mod = (await import(pathToFileURL(configPath).href)) as {
    default?: WorkflowConfig;
  };
  const services = mod.default?.services ?? {};
  const serviceNames = Object.keys(services);

  const content = renderServicesDts(serviceNames);
  const outPath = join(root, ".lorien", "types", "services.d.ts");
  await mkdir(dirname(outPath), { recursive: true });
  await writeFile(outPath, content, "utf-8");

  return { path: outPath, serviceNames };
}

export function renderServicesDts(serviceNames: string[]): string {
  const lines: string[] = [];
  lines.push(
    "// .lorien/types/services.d.ts (auto-generated by @darrylondil/lorien-build, do not edit)",
  );
  lines.push(
    "// v1.0: all services typed as `unknown`. Refine manually or upgrade to v1.1 for TS-compiler inference.",
  );
  lines.push("");
  lines.push(`declare module "@darrylondil/lorien-runtime" {`);
  lines.push("  interface Services {");
  for (const name of serviceNames) {
    lines.push(`    ${name}: unknown // TODO: refine this type`);
  }
  lines.push("  }");
  lines.push("}");
  lines.push("");
  lines.push("export {}");
  lines.push("");
  return lines.join("\n");
}

async function fileExists(p: string): Promise<boolean> {
  try {
    const s = await stat(p);
    return s.isFile();
  } catch {
    return false;
  }
}
