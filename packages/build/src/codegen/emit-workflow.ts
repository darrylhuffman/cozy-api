import { resolveInputValue, type WorkflowFile } from "@darrylondil/lorien-runtime"

/**
 * Result of generating a single workflow's .gen.ts source.
 */
export interface EmitWorkflowResult {
  /** The generated TypeScript source. */
  source: string
  /** The user node `uses` strings imported by this workflow (e.g. "./nodes/foo"). */
  importedNodes: string[]
}

/**
 * Options for emitWorkflow.
 */
export interface EmitWorkflowOptions {
  /** The parsed workflow file. */
  workflow: WorkflowFile
  /**
   * Path under workflows/ (without `.workflow` extension), used to compute
   * the depth of the generated file. e.g. "users/create" or "users/[id]/get".
   * The generated file lives at "dist/workflows/<relPath>.gen.ts" so it walks
   * up `depth(relPath) + 1` (for "workflows/") directories to reach the
   * project root, where lorien.config and nodes/ live.
   */
  relativePath: string
}

/**
 * Given a parsed workflow, emit the .gen.ts source string that exports
 * `function register(app: Hono): void`.
 *
 * The emitted code has zero @darrylondil/lorien-runtime imports. Only hono and the
 * user's own node modules are imported.
 */
export function emitWorkflow(opts: EmitWorkflowOptions): EmitWorkflowResult {
  const { workflow, relativePath } = opts

  // dist/workflows/<relativePath>.gen.ts → root is `dist/..`
  // Depth = number of path segments in relativePath (each is a parent dir to climb)
  //   + 1 for the "workflows/" parent that wraps every .gen.ts.
  const depth = relativePath.split("/").length + 1
  const upPrefix = "../".repeat(depth)

  // Collect all user-defined `uses` strings referenced by this workflow.
  const userUses = new Set<string>()
  for (const inst of Object.values(workflow.nodes)) {
    if (!inst.uses.startsWith("@core/")) {
      userUses.add(inst.uses)
    }
  }

  // Find all http-request triggers in this workflow.
  const triggers: TriggerInfo[] = []
  for (const [nodeId, inst] of Object.entries(workflow.nodes)) {
    if (inst.uses !== "@core/http-request") continue
    const config = (inst.config ?? {}) as { path?: string; method?: string }
    triggers.push({
      nodeId,
      path: config.path ?? "/",
      method: (config.method ?? "GET").toUpperCase(),
    })
  }

  // Build a stable map from `uses` to local identifier.
  const sortedUses = [...userUses].sort()
  const usesToIdent = new Map<string, string>()
  const seenIdents = new Set<string>()
  for (const uses of sortedUses) {
    let ident = importIdentForUses(uses)
    // Collision-safety: append a counter if necessary.
    if (seenIdents.has(ident)) {
      let i = 2
      while (seenIdents.has(`${ident}${i}`)) i++
      ident = `${ident}${i}`
    }
    seenIdents.add(ident)
    usesToIdent.set(uses, ident)
  }

  const lines: string[] = []
  lines.push(`// AUTO-GENERATED by @darrylondil/lorien-build. Do not edit.`)
  lines.push(`import type { Hono, Context } from "hono"`)
  lines.push(`import config from "${upPrefix}lorien.config.js"`)
  for (const uses of sortedUses) {
    const ident = usesToIdent.get(uses)!
    const importPath = nodeImportPath(uses, upPrefix)
    lines.push(`import ${ident} from "${importPath}"`)
  }
  lines.push("")
  lines.push(renderResolveServicesHelper())
  lines.push("")
  lines.push(renderReadJsonBodyHelper())
  lines.push("")
  lines.push(`export function register(app: Hono): void {`)

  for (let i = 0; i < triggers.length; i++) {
    const trigger = triggers[i]!
    if (i > 0) lines.push("")
    lines.push(...renderHandler(workflow, trigger, usesToIdent))
  }

  lines.push(`}`)
  lines.push("")

  return {
    source: lines.join("\n"),
    importedNodes: sortedUses,
  }
}

interface TriggerInfo {
  nodeId: string
  path: string
  method: string
}

function renderResolveServicesHelper(): string {
  return [
    `function resolveServices(ctx: { requestId: string; timestamp: number }) {`,
    `  const services: Record<string, unknown> = {}`,
    `  const declared = (config.services ?? {}) as Record<string, unknown>`,
    `  for (const [name, value] of Object.entries(declared)) {`,
    `    services[name] =`,
    `      typeof value === "function" ? (value as (c: typeof ctx) => unknown)(ctx) : value`,
    `  }`,
    `  return services`,
    `}`,
  ].join("\n")
}

function renderReadJsonBodyHelper(): string {
  return [
    `async function readJsonBody(c: Context): Promise<unknown> {`,
    `  const contentType = c.req.header("content-type") ?? ""`,
    `  if (contentType.includes("application/json")) {`,
    `    try {`,
    `      return await c.req.json()`,
    `    } catch {`,
    `      return null`,
    `    }`,
    `  }`,
    `  if (c.req.raw.body) {`,
    `    try {`,
    `      return await c.req.text()`,
    `    } catch {`,
    `      return null`,
    `    }`,
    `  }`,
    `  return null`,
    `}`,
  ].join("\n")
}

function renderHandler(
  workflow: WorkflowFile,
  trigger: TriggerInfo,
  usesToIdent: Map<string, string>,
): string[] {
  const lines: string[] = []
  const triggerVar = outputsVar(trigger.nodeId)

  const depsByNode = buildDepsByNode(workflow)
  const sliceIds = computeTriggerSlice(workflow, trigger.nodeId, depsByNode)

  // Project deps onto the slice for wave computation.
  const slicedDeps = new Map<string, Set<string>>()
  for (const id of sliceIds) {
    const filtered = new Set<string>()
    for (const d of depsByNode.get(id) ?? []) {
      if (sliceIds.has(d)) filtered.add(d)
    }
    slicedDeps.set(id, filtered)
  }
  const waves = computeWaves(slicedDeps)
  const hasResponse = hasResponseInSlice(workflow, sliceIds)

  lines.push(
    `  app.on("${trigger.method}", "${trigger.path}", async (c: Context): Promise<Response> => {`,
  )
  lines.push(`    const requestId = crypto.randomUUID()`)
  lines.push(`    const services = resolveServices({ requestId, timestamp: Date.now() })`)
  lines.push("")
  lines.push(`    // Trigger outputs`)
  lines.push(`    const ${triggerVar} = {`)
  lines.push(`      body: await readJsonBody(c),`)
  lines.push(`      params: c.req.param(),`)
  lines.push(`      query: Object.fromEntries(new URL(c.req.url).searchParams.entries()),`)
  lines.push(`      headers: Object.fromEntries(c.req.raw.headers.entries()),`)
  lines.push(`      context: { requestId, timestamp: Date.now() },`)
  lines.push(`    }`)

  let waveNum = 0
  for (const wave of waves) {
    const interesting = wave.filter((id) => id !== trigger.nodeId)
    if (interesting.length === 0) continue
    waveNum++

    const responseIds = interesting.filter((id) => workflow.nodes[id]?.uses === "@core/response")
    const computeIds = interesting.filter((id) => workflow.nodes[id]?.uses !== "@core/response")

    if (computeIds.length === 1) {
      lines.push("")
      lines.push(`    // Wave ${waveNum}: ${computeIds[0]}`)
      lines.push(...renderSingleNodeCall(workflow, computeIds[0]!, usesToIdent, "    "))
    } else if (computeIds.length > 1) {
      lines.push("")
      lines.push(`    // Wave ${waveNum}: ${computeIds.join(", ")} (parallel)`)
      lines.push(...renderParallelWave(workflow, computeIds, usesToIdent, "    "))
    }

    if (responseIds.length > 0) {
      // First response in the wave wins (sorted ordering for determinism).
      const responseId = responseIds[0]!
      lines.push("")
      lines.push(`    // Response`)
      lines.push(...renderResponseReturn(workflow, responseId, "    "))
      break
    }
  }

  if (!hasResponse) {
    lines.push("")
    lines.push(`    return new Response("null", {`)
    lines.push(`      status: 200,`)
    lines.push(`      headers: { "content-type": "application/json" },`)
    lines.push(`    })`)
  }

  lines.push(`  })`)
  return lines
}

function hasResponseInSlice(workflow: WorkflowFile, sliceIds: Set<string>): boolean {
  for (const id of sliceIds) {
    if (workflow.nodes[id]?.uses === "@core/response") return true
  }
  return false
}

function renderSingleNodeCall(
  workflow: WorkflowFile,
  nodeId: string,
  usesToIdent: Map<string, string>,
  indent: string,
): string[] {
  const inst = workflow.nodes[nodeId]!
  const ident = usesToIdent.get(inst.uses)
  if (!ident) throw new Error(`emit-workflow: no identifier for uses \`${inst.uses}\``)
  const outVar = outputsVar(nodeId)
  const inputVar = `_${nodeId}Input`
  const inputRawVar = `_${nodeId}InputRaw`
  const inputExpr = renderInputExpr(inst.in)
  const configExpr = inst.config !== undefined ? renderJsonLiteral(inst.config) : "undefined"

  // Whole-object form: source is a reference expression (already a JS access
  // chain). Emit a raw alias for clarity, then run through inputs.parse().
  if (typeof inst.in === "string") {
    return [
      `${indent}const ${inputRawVar} = ${inputExpr}`,
      `${indent}const ${inputVar} = ${ident}.inputs.parse(${inputRawVar})`,
      `${indent}const ${outVar} = (await ${ident}.run(`,
      `${indent}  ${inputVar} as never,`,
      `${indent}  services as never,`,
      `${indent}  ${configExpr} as never,`,
      `${indent})) as Record<string, unknown>`,
    ]
  }

  return [
    `${indent}const ${inputVar} = ${ident}.inputs.parse(${inputExpr})`,
    `${indent}const ${outVar} = (await ${ident}.run(`,
    `${indent}  ${inputVar} as never,`,
    `${indent}  services as never,`,
    `${indent}  ${configExpr} as never,`,
    `${indent})) as Record<string, unknown>`,
  ]
}

function renderParallelWave(
  workflow: WorkflowFile,
  nodeIds: string[],
  usesToIdent: Map<string, string>,
  indent: string,
): string[] {
  const lines: string[] = []
  // Emit inputs.parse() for each node before the allSettled block.
  for (const id of nodeIds) {
    const inst = workflow.nodes[id]!
    const ident = usesToIdent.get(inst.uses)
    if (!ident) throw new Error(`emit-workflow: no identifier for uses \`${inst.uses}\``)
    const inputVar = `_${id}Input`
    const inputExpr = renderInputExpr(inst.in)
    lines.push(`${indent}const ${inputVar} = ${ident}.inputs.parse(${inputExpr})`)
  }
  const settledNames = nodeIds.map((id) => `${id}_settled`)
  lines.push(`${indent}const [${settledNames.join(", ")}] = await Promise.allSettled([`)
  for (const id of nodeIds) {
    const inst = workflow.nodes[id]!
    const ident = usesToIdent.get(inst.uses)
    if (!ident) throw new Error(`emit-workflow: no identifier for uses \`${inst.uses}\``)
    const inputVar = `_${id}Input`
    const configExpr = inst.config !== undefined ? renderJsonLiteral(inst.config) : "undefined"
    lines.push(`${indent}  ${ident}.run(${inputVar} as never, services as never, ${configExpr} as never),`)
  }
  lines.push(`${indent}])`)
  lines.push(
    `${indent}const _rejection = [${settledNames.join(", ")}].find((r) => r.status === "rejected")`,
  )
  lines.push(`${indent}if (_rejection) throw (_rejection as PromiseRejectedResult).reason`)
  for (const id of nodeIds) {
    const outVar = outputsVar(id)
    lines.push(
      `${indent}const ${outVar} = (${id}_settled as PromiseFulfilledResult<unknown>).value as Record<string, unknown>`,
    )
  }
  return lines
}

function renderResponseReturn(workflow: WorkflowFile, nodeId: string, indent: string): string[] {
  const inst = workflow.nodes[nodeId]!

  let bodyExpr: string
  let statusExpr: string
  let headersExpr: string

  if (typeof inst.in === "string") {
    // Whole-object form: the resolved value is the input bag itself; pluck
    // body/status/headers fields off it directly.
    const base = renderInputExpr(inst.in)
    bodyExpr = `(${base})?.body`
    statusExpr = `(${base})?.status`
    headersExpr = `(${base})?.headers`
  } else {
    const inMap = inst.in ?? {}
    bodyExpr = "body" in inMap ? renderInputValue(inMap.body) : `null`
    statusExpr = "status" in inMap ? renderInputValue(inMap.status) : `200`
    headersExpr = "headers" in inMap ? renderInputValue(inMap.headers) : `{}`
  }

  return [
    `${indent}const _bodyValue = ${bodyExpr}`,
    `${indent}return new Response(`,
    `${indent}  _bodyValue === undefined ? "null" : JSON.stringify(_bodyValue),`,
    `${indent}  {`,
    `${indent}    status: ((${statusExpr}) as number | undefined) ?? 200,`,
    `${indent}    headers: {`,
    `${indent}      "content-type": "application/json",`,
    `${indent}      ...(((${headersExpr}) as Record<string, string> | undefined) ?? {}),`,
    `${indent}    },`,
    `${indent}  },`,
    `${indent})`,
  ]
}

/**
 * Render an `in: { ... }` object as a JS object literal expression.
 */
function renderInputObject(inMap: Record<string, unknown>): string {
  const entries = Object.entries(inMap)
  if (entries.length === 0) return `{}`
  const parts: string[] = []
  for (const [k, v] of entries) {
    parts.push(`${jsKey(k)}: ${renderInputValue(v)}`)
  }
  return `{ ${parts.join(", ")} }`
}

/**
 * Render `in:` of either form (string reference for whole-object, or per-field
 * object) as a JS expression suitable for passing to `inputs.parse(...)`.
 */
function renderInputExpr(inField: string | Record<string, unknown> | undefined): string {
  if (typeof inField === "string") {
    // Whole-object reference: must be a parseable reference (validated upstream).
    return renderInputValue(inField)
  }
  return renderInputObject(inField ?? {})
}

/**
 * Render a single `in:` value as a JS expression — reference or literal.
 */
function renderInputValue(raw: unknown): string {
  const resolved = resolveInputValue(raw)
  if (resolved.kind === "reference") {
    const { nodeId, path } = resolved.ref
    const base = outputsVar(nodeId)
    if (path.length === 0) return base
    return base + path.map((seg) => `.${seg}`).join("")
  }
  return renderJsonLiteral(resolved.value)
}

/**
 * Render an arbitrary JSON-serializable value as a JS literal expression.
 */
function renderJsonLiteral(value: unknown): string {
  if (value === undefined) return `undefined`
  if (value === null) return `null`
  if (typeof value === "string") return JSON.stringify(value)
  if (typeof value === "number" || typeof value === "boolean") return String(value)
  if (typeof value === "bigint") return `${value.toString()}n`
  if (Array.isArray(value)) {
    return `[${value.map(renderJsonLiteral).join(", ")}]`
  }
  if (typeof value === "object") {
    const entries = Object.entries(value as Record<string, unknown>)
    if (entries.length === 0) return `{}`
    return `{ ${entries.map(([k, v]) => `${jsKey(k)}: ${renderJsonLiteral(v)}`).join(", ")} }`
  }
  return `null`
}

function jsKey(key: string): string {
  if (/^[a-zA-Z_$][\w$]*$/.test(key)) return key
  return JSON.stringify(key)
}

function nodeImportPath(uses: string, upPrefix: string): string {
  const cleaned = uses.startsWith("./") ? uses.slice(2) : uses
  return `${upPrefix}${cleaned}.js`
}

function importIdentForUses(uses: string): string {
  const base = uses.split("/").pop() ?? uses
  const parts = base.split("-").filter((s) => s.length > 0)
  if (parts.length === 0) return "node_default"
  const [first, ...rest] = parts
  const ident = first! + rest.map((p) => p[0]!.toUpperCase() + p.slice(1)).join("")
  if (!/^[a-zA-Z_$]/.test(ident)) return `_${ident}`
  return ident
}

function outputsVar(nodeId: string): string {
  if (/^[a-zA-Z_$][\w$]*$/.test(nodeId)) return `${nodeId}_outputs`
  const safe = nodeId.replace(/[^a-zA-Z0-9_$]/g, "_")
  return `${safe}_outputs`
}

// --- Graph helpers (mirror runtime's logic, kept local so codegen doesn't
//     have to import runtime internals).

function buildDepsByNode(wf: WorkflowFile): Map<string, Set<string>> {
  const depsByNode = new Map<string, Set<string>>()
  for (const [id, inst] of Object.entries(wf.nodes)) {
    const deps = new Set<string>()
    if (inst.in !== undefined) {
      if (typeof inst.in === "string") {
        const resolved = resolveInputValue(inst.in)
        if (resolved.kind === "reference") {
          if (wf.nodes[resolved.ref.nodeId]) deps.add(resolved.ref.nodeId)
        }
      } else {
        for (const raw of Object.values(inst.in)) {
          const resolved = resolveInputValue(raw)
          if (resolved.kind === "reference") {
            if (wf.nodes[resolved.ref.nodeId]) deps.add(resolved.ref.nodeId)
          }
        }
      }
    }
    if (inst.after) {
      for (const t of inst.after) {
        if (wf.nodes[t]) deps.add(t)
      }
    }
    depsByNode.set(id, deps)
  }
  return depsByNode
}

function computeWaves(depsByNode: Map<string, Set<string>>): string[][] {
  const remaining = new Map<string, Set<string>>()
  for (const [id, deps] of depsByNode) {
    remaining.set(id, new Set(deps))
  }
  const waves: string[][] = []
  while (remaining.size > 0) {
    const wave: string[] = []
    for (const [id, deps] of remaining) {
      if (deps.size === 0) wave.push(id)
    }
    if (wave.length === 0) {
      throw new Error("computeWaves: stuck (possible cycle — validate first)")
    }
    wave.sort()
    waves.push(wave)
    for (const id of wave) {
      remaining.delete(id)
      for (const deps of remaining.values()) deps.delete(id)
    }
  }
  return waves
}

function computeTriggerSlice(
  wf: WorkflowFile,
  triggerNodeId: string,
  depsByNode: Map<string, Set<string>>,
): Set<string> {
  const allIds = Object.keys(wf.nodes)
  const downstreamOf = new Map<string, Set<string>>()
  for (const id of allIds) downstreamOf.set(id, new Set())
  for (const [id, deps] of depsByNode) {
    for (const d of deps) downstreamOf.get(d)?.add(id)
  }

  const allTriggers = allIds.filter((id) => wf.nodes[id]?.uses === "@core/http-request")
  const reachableFrom = new Map<string, Set<string>>()
  for (const tid of allTriggers) {
    const reach = new Set<string>([tid])
    const q = [tid]
    while (q.length > 0) {
      const cur = q.shift()!
      for (const n of downstreamOf.get(cur) ?? []) {
        if (!reach.has(n)) {
          reach.add(n)
          q.push(n)
        }
      }
    }
    reachableFrom.set(tid, reach)
  }

  const ownedByAny = new Set<string>()
  for (const r of reachableFrom.values()) for (const id of r) ownedByAny.add(id)

  const myReach = reachableFrom.get(triggerNodeId) ?? new Set([triggerNodeId])
  const hasExplicitResponse = [...myReach].some(
    (id) => id !== triggerNodeId && wf.nodes[id]?.uses === "@core/response",
  )

  const included = new Set<string>()
  for (const id of allIds) {
    if (myReach.has(id)) {
      included.add(id)
    } else if (!ownedByAny.has(id)) {
      const isResponse = wf.nodes[id]?.uses === "@core/response"
      if (!isResponse || !hasExplicitResponse) included.add(id)
    }
  }
  return included
}
