/**
 * Result of generating dist/index.ts.
 */
export interface EmitIndexResult {
  source: string
}

/**
 * Options for emitIndex.
 */
export interface EmitIndexOptions {
  /**
   * Relative paths (under workflows/) of every workflow to be wired into the
   * server. Without `.workflow` extension. e.g. ["users/create", "users/[id]/get"].
   * Path separators must be forward slashes.
   */
  workflowPaths: string[]
}

/**
 * Emit dist/index.ts — the master file that boots a single Hono app and
 * registers every per-workflow .gen.ts module onto it.
 */
export function emitIndex(opts: EmitIndexOptions): EmitIndexResult {
  // Sort for deterministic output regardless of input order.
  const sorted = [...opts.workflowPaths].sort()

  const lines: string[] = []
  lines.push(`// AUTO-GENERATED by @cozy/build. Do not edit.`)
  lines.push(`import { Hono } from "hono"`)
  lines.push(`import { serve } from "@hono/node-server"`)

  const idents: string[] = []
  const seen = new Set<string>()
  for (const path of sorted) {
    let ident = `register_${slugIdentifier(path)}`
    if (seen.has(ident)) {
      let i = 2
      while (seen.has(`${ident}_${i}`)) i++
      ident = `${ident}_${i}`
    }
    seen.add(ident)
    idents.push(ident)
    const importPath = `./workflows/${slugifyPath(path)}.gen.js`
    lines.push(`import { register as ${ident} } from "${importPath}"`)
  }

  lines.push("")
  lines.push(`const app = new Hono()`)
  for (const ident of idents) {
    lines.push(`${ident}(app)`)
  }
  lines.push("")
  lines.push(`const port = Number(process.env.PORT) || 3000`)
  lines.push(`serve({ fetch: app.fetch, port }, ({ port }) => {`)
  // biome-ignore lint/suspicious/noTemplateCurlyInString: emitted as-is into generated code; placeholder is for the user's runtime, not ours.
  lines.push("  console.log(`cozy-api listening on http://localhost:${port}`)")
  lines.push(`})`)
  lines.push("")

  return { source: lines.join("\n") }
}

/**
 * Convert a workflow's relative path into a safe path used on disk for
 * the generated .gen.ts file. Replaces dynamic-route segments like `[id]`
 * with a safe form so the path is filesystem-safe AND the import works.
 *
 * For now we keep the original characters EXCEPT we slugify just the
 * pieces that would conflict with module-resolution. Brackets are allowed
 * in most filesystems and in Node import paths, so we keep them by default.
 *
 * If you want fully sluggy filenames, change this to mirror slugIdentifier.
 */
export function slugifyPath(relativePath: string): string {
  // Replace bracket segments [id] -> _id_  for cross-filesystem & shell safety.
  return relativePath
    .split("/")
    .map((seg) => seg.replace(/\[([^\]]+)\]/g, "_$1_"))
    .join("/")
}

/**
 * Convert a workflow relative path into a JS identifier suffix.
 *
 *   "users/create"        -> "users_create"
 *   "users/[id]/get"      -> "users_id_get"
 *   "v1/users.list"       -> "v1_users_list"
 */
export function slugIdentifier(relativePath: string): string {
  // Replace bracket segments [foo] -> foo (drop the brackets entirely for ident).
  const noBrackets = relativePath.replace(/\[([^\]]+)\]/g, "$1")
  // Replace any non-ident character with underscore.
  const cleaned = noBrackets.replace(/[^a-zA-Z0-9_$]/g, "_")
  // Collapse runs of underscores; trim leading/trailing.
  const collapsed = cleaned.replace(/_+/g, "_").replace(/^_+|_+$/g, "")
  // Ensure it doesn't start with a digit.
  if (/^[0-9]/.test(collapsed)) return `_${collapsed}`
  return collapsed.length > 0 ? collapsed : "workflow"
}
